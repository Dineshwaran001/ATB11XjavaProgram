package ex_03_Operators;

public class Lab008_ArithmeticOperator {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        float c = 9f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c); //if you divie something by float the answer will be float
    }
}
