package ex_03_Operators;

public class Lab017_Compound_AssignmentOperator {
    public static void main(String[] args) {
        int age =20;
        age +=10;// this means age = age-->20 +10 =30
        System.out.println(age);

        int age1= 20;
        age1 -=10; //age1 = age1 -10 --> 20-10=10
        System.out.println(age1);


    }
}
