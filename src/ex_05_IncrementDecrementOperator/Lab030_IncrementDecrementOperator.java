package ex_05_IncrementDecrementOperator;

public class Lab030_IncrementDecrementOperator {
    public static void main(String[] args) {
        int a =10; // a value is 10
        // it first addition the value and assign it to b
        int b = ++a; // in this line a value change to 11 and b value also 11
        System.out.println(b); // here it is 11
        System.out.println(a); // here it is 11

        int  c =20;
        // it assign value c to d and add for c
        int d= c++;
        System.out.println(c);
        System.out.println(d);



    }
}
